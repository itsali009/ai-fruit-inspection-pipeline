# Importing necessary libraries
import tensorflow as tf
from tensorflow.keras.applications import EfficientNetB3
from tensorflow.keras.preprocessing.image import ImageDataGenerator
from tensorflow.keras.layers import Dense, GlobalAveragePooling2D
from tensorflow.keras.models import Model
from sklearn.metrics import classification_report, confusion_matrix
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

"""# Step 2: Load and Preprocess the Data
Use ImageDataGenerator to load and preprocess the data. The dataset will be divided into training and validation sets.
"""

# Define paths for training and validation datasets
train_dir = '/content/drive/MyDrive/Fruits-Variety/Training'
valid_dir = '/content/drive/MyDrive/Fruits-Variety/Validation'

# Define image size and batch size
IMG_SIZE = (300, 300)
BATCH_SIZE = 32

# Data augmentation and rescaling for training data
train_datagen = ImageDataGenerator(
    rescale=1./255,
    rotation_range=20,
    width_shift_range=0.2,
    height_shift_range=0.2,
    zoom_range=0.2,
    horizontal_flip=True,
    fill_mode='nearest'
)

# Rescaling validation data
valid_datagen = ImageDataGenerator(rescale=1./255)

# Loading training data
train_generator = train_datagen.flow_from_directory(
    train_dir,
    target_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='categorical'
)

# Loading validation data
valid_generator = valid_datagen.flow_from_directory(
    valid_dir,
    target_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='categorical'
)

"""## Step 3: Build the EfficientNetB3 Model
Load the EfficientNetB3 model, pretrained on ImageNet, and fine-tune it for specific classification task.
"""

# Load the EfficientNetB3 model with pre-trained ImageNet weights
base_model = EfficientNetB3(weights='imagenet', include_top=False, input_shape=(300, 300, 3))

# Freeze the base model layers
base_model.trainable = False

# Add custom layers on top of EfficientNetB3
x = base_model.output
x = GlobalAveragePooling2D()(x)  # Global pooling layer
x = Dense(512, activation='relu')(x)  # Fully connected layer
predictions = Dense(train_generator.num_classes, activation='softmax')(x)  # Output layer

# Create the full model
model = Model(inputs=base_model.input, outputs=predictions)

# Compile the model
model.compile(optimizer='adam', loss='categorical_crossentropy', metrics=['accuracy'])

# Model summary
model.summary()

"""## Step 4: Train the Model
Train the model using the training and validation datasets.
"""

# Training the model
EPOCHS = 10

history = model.fit(
    train_generator,
    steps_per_epoch=train_generator.samples // BATCH_SIZE,
    validation_data=valid_generator,
    validation_steps=valid_generator.samples // BATCH_SIZE,
    epochs=EPOCHS
)

"""## Step 5: Evaluate the Model
After training, evaluate the model using accuracy, precision, recall, F1-score, and confusion matrix.
"""

# Get predictions and true labels
validation_steps = valid_generator.samples // BATCH_SIZE + 1
Y_pred = model.predict(valid_generator, validation_steps=validation_steps)
y_pred = np.argmax(Y_pred, axis=1)
y_true = valid_generator.classes

# Classification report
print("Classification Report:")
print(classification_report(y_true, y_pred, target_names=valid_generator.class_indices.keys()))

# Confusion matrix
conf_matrix = confusion_matrix(y_true, y_pred)

# Plot confusion matrix
plt.figure(figsize=(10, 8))
sns.heatmap(conf_matrix, annot=True, fmt='d', cmap='Blues', xticklabels=valid_generator.class_indices.keys(), yticklabels=valid_generator.class_indices.keys())
plt.xlabel('Predicted Label')
plt.ylabel('True Label')
plt.title('Confusion Matrix')
plt.show()

"""## Step 6: Plot Accuracy and Loss Curves
Visualize the model’s training and validation accuracy and loss over the epochs.
"""

# Plot accuracy and loss over epochs
acc = history.history['accuracy']
val_acc = history.history['val_accuracy']
loss = history.history['loss']
val_loss = history.history['val_loss']

epochs_range = range(EPOCHS)

plt.figure(figsize=(12, 6))

# Plot accuracy
plt.subplot(1, 2, 1)
plt.plot(epochs_range, acc, label='Training Accuracy')
plt.plot(epochs_range, val_acc, label='Validation Accuracy')
plt.legend(loc='lower right')
plt.title('Training and Validation Accuracy')

# Plot loss
plt.subplot(1, 2, 2)
plt.plot(epochs_range, loss, label='Training Loss')
plt.plot(epochs_range, val_loss, label='Validation Loss')
plt.legend(loc='upper right')
plt.title('Training and Validation Loss')

plt.show()

"""## Step 7: Save the Model
Finally, save the trained model for future use.
"""

# Save the model
model.save('efficientnetb3_fruit_variety_classifier.h5')